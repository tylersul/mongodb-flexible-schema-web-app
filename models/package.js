let mongoose = require("mongoose");

// ================================================================== //
// ====================== Schema Definition ========================= //
// ================================================================== //
let packageSchema = new mongoose.Schema({
   id: String,
   rfid: String,
   status: String,
   live: String
});

// ================================================================== //
// ====================== Exports =================================== //
// ================================================================== //
module.exports = mongoose.model("Package", packageSchema);